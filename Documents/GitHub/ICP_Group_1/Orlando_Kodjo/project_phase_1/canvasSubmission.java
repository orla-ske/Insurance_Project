public class canvasSubmission {
    String grade;
    String submission;
    String submitter;

    public canvasSubmission(String Submission,String Submitter){
        submission=Submission;
        submitter=Submitter;
    }

    public String getSubmitter(){
        return grade;
    }

    public String getGrade(){
        return grade;
    }

    public String getSubmission(){
        return submission;
    }

    public void setGrade(String Grade){
        grade= Grade;
    }
}
